package uk.org.datalink.MDR.domain;

import java.util.Date;

public class PermissibleValue {


	  private int id;
	  private String permittedvalue;
	  private Date valuebegindate;
	  private Date valueenddate;
	  
	  public void setId(int nid){
		  this.id = nid;
	  }
	  public void setPermittedvalue(String pv){
		  this.permittedvalue = pv;
	  }
	  public void setValuebegindate(Date sd){
		  this.valuebegindate = sd;
	  }
	  public void setValueenddate(Date ed){
		  this.valueenddate = ed;
	  }
	  
	  
	  public int getId(){
		  return this.id;
	  }
	  public String getPermittedvalue(){
		  return this.permittedvalue;
	  }
	  public Date getValuebegindate(){
		  return this.valuebegindate;
	  }
	  public Date getValueenddate(){
		  return this.valueenddate;
	  }
	  
}
