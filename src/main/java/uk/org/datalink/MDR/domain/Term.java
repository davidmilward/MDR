package uk.org.datalink.MDR.domain;

public class Term {

	private String term;
	private String description;
	private int id;
	
	public void setTerm(String tm){
		this.term = tm;
	}
	public void setDescription(String dc){
		this.description = dc;
	}
	public void setId(int tmid){
		this.id = tmid;
	}
	
	public String getTerm(){
		return this.term;
	}
	public String getDescription(){
		return this.description;
	}
	public int getId(){
		return this.id;
	}
	
}
