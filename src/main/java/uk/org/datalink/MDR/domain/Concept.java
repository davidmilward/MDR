package uk.org.datalink.MDR.domain;

public class Concept extends AdminObject {
	
	  /**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	  private int id;
	  private String description;
	  private int termid;
	  private int adminobjectid;
	  
	  public void setId(int nid){
		  this.id = nid;
	  }
	  public void setDescription(String desc){
		  this.description = desc;
	  }
	  public void setTermid(int tid){
		  this.termid = tid;
	  }
	  public void setAdminobjectid(int adid){
		  this.adminobjectid = adid;
	  }
	  
	  public int getId(){
		  return this.id;
	  }
	  public String getDescription(){
		  return this.description;
	  }
	  public int getTermid(){
		  return this.termid;
	  }
	  public int getAdminobjectid(){
		  return this.adminobjectid;
	  }
	  
	  
}
