package uk.org.datalink.MDR.domain;

public class EnumeratedDomains {
	
	  private int id;
	  private String ensname;
	  private int permissiblevalueid;

	  
	  public void setId(int nid){
		  this.id = nid;
	  }
	  public void setEnsname(String desc){
		  this.ensname = desc;
	  }
	  public void setPermissiblevalueid(int tid){
		  this.permissiblevalueid = tid;
	  }

	  
	  public int getId(){
		  return this.id;
	  }
	  public String getEnsname(){
		  return this.ensname;
	  }
	  public int getPermissiblevalueid(){
		  return this.permissiblevalueid;
	  }
	  
	
}
